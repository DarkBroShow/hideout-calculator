# Комиссия аукциона при продаже
AUCTION_COMMISSION: float = 0.05

# ---------------------------------------------------------------------------
# Адаптивный TTL цен (используется при пересчёте кэша)
# Пороги (продаж/день, TTL в секундах), от большего к меньшему
# ---------------------------------------------------------------------------
TTL_TIERS: list[tuple[int, int]] = [
    (100, 15 * 60),      # ≥ 100 продаж/день → 15 мин
    (10,  60 * 60),      # ≥ 10              → 1 час
    (1,   6 * 60 * 60),  # ≥ 1               → 6 часов
    (0,   24 * 60 * 60), # < 1               → 24 часа
]

# ---------------------------------------------------------------------------
# Фоновый коллектор цен (PriceCollector)
# ---------------------------------------------------------------------------

# Пауза между полными циклами обхода всех предметов (секунды)
COLLECTOR_CYCLE_INTERVAL_SECONDS: int = 60

# Задержка между отдельными запросами к API внутри цикла (секунды)
# Снижает нагрузку на API и расход токенов
COLLECTOR_DELAY_BETWEEN_REQUESTS: float = 0.5

# Максимальное количество предметов за один цикл
# (остальные подождут следующего цикла)
COLLECTOR_BATCH_SIZE: int = 30

# Лимит записей при первичном (холодном) сборе истории — нет данных совсем
COLLECTOR_HISTORY_LIMIT_COLD: int = 190

# Лимит записей при инкрементальном обновлении истории
COLLECTOR_HISTORY_LIMIT_INCREMENTAL: int = 20

# Окно актуальности истории (дни): если последняя запись старше — делаем холодный fetch
COLLECTOR_HISTORY_WINDOW_DAYS: int = 2

# Максимальный offset при инкрементальном обходе (защита от бесконечного цикла)
COLLECTOR_HISTORY_MAX_OFFSET: int = 400

# Лимит активных лотов при сборе buy_price
COLLECTOR_LOTS_LIMIT: int = 50

# Вес запросов пользователей в расчёте приоритета (множитель request_count)
# Увеличьте, чтобы «горячие» предметы обновлялись агрессивнее
COLLECTOR_REQUEST_BOOST_WEIGHT: float = 5.0

# Окно «свежего запроса» (секунды): только запросы не старше этого времени
# поднимают приоритет (защита от накопленного request_count)
COLLECTOR_REQUEST_RECENCY_WINDOW: int = 3600  # 1 час

# Сколько секунд ждать после TokenExhaustedError перед следующей попыткой
COLLECTOR_TOKEN_WAIT_SECONDS: int = 60

# Через сколько дней перепроверять предметы, помеченные как auction_available=False
# (вдруг позже появятся на аукционе)
COLLECTOR_NO_AUCTION_RECHECK_DAYS: int = 7

